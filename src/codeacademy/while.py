'''
while and next loop are pretty similar
'''
condition = 1       #   variable start, base case
while  condition < 10:  #  assignment operator
    print (condition)
    condition += 1  # prints the variable from 1 to 9 and stops at 10

while True:     #infinite loop using a boolean test
    print ('doing stuff')
